str = input()

print("Alpha: ", str.isalpha())# so letras
print("Alfanum: ", str.isalnum())# letras e numeros
print("Numeric: ", str.isnumeric())#se é todo numerico
print("Is uppercase: ", str.isupper())#Se tudo está maiusculo
print("Is lowercase: ", str.islower()) #Se tudo esta minusculo
print("Is capitalize: ", str.istitle()) #Se a string começa com letra maiuscula e o resto minuscula
print("Only whitespace: ", str.isspace()) #Se a variavel esta cheia de espaços

